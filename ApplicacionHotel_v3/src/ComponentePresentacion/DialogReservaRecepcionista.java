    public void buscar(){
        String idRecepcionista = txtIdRecepcionista.getText();
        try {
            List<Reserva> reserva = ReservaCN.getInstancia().ListadoReservasPorRecepcionista(idRecepcionista);
            Object datos[][] = new Object[reserva.size()][5];
            for(int i = 0 ; i < reserva.size(); i++){
                Reserva reservas = reserva.get(i);
                datos[i][0] = reservas.getRecepcionista().getIdRecepcionista(); 
                datos[i][1] = reservas.getRecepcionista().getNombres();
                datos[i][2] = reservas.getRecepcionista().getApellidos();
                datos[i][3] = reservas.getFecha();
                datos[i][3] = reservas.getIdReserva();
                datos[i][4] = reservas.CalcularCosto();
            }
            modelo.setDataVector(datos, nombreColumna);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
    }
