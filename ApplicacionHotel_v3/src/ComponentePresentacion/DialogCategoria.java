    public void buscar() {
        try {
            List<Categoria> categorias = CategoriaCN.getInstancia().lista();
            Object datos[][] = new Object[categorias.size()][3];
            for (int i = 0; i < categorias.size(); i++) {
                Categoria categoria = categorias.get(i);
                datos[i][0] = categoria.getIdCategoria();
                datos[i][1] = categoria.getNombre();
                datos[i][2] = categoria.getDescripcion();
            }
            modelo.setDataVector(datos, nombreColumnas);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
    }


