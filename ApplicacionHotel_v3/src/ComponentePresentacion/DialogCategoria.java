    private void btnRegirtrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegirtrarActionPerformed
       if(txtIdCategoria.getText().compareTo("")!=0){ 
        String codigo = txtIdCategoria.getText();
        String des = txtDes.getText();
        String nombre = txtNombre.getText();
        Categoria categoria = new Categoria(codigo, des, nombre);
        try {
            if (reg) {
                CategoriaCN.getInstancia().crearCategoria(categoria);
                JOptionPane.showMessageDialog(rootPane, "Se registro sus datos.");
            } else {
                JOptionPane.showMessageDialog(rootPane, "no se registro los Datos");
            }
            buscar();
            limpiarTextos();
        

        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
       } else
        JOptionPane.showMessageDialog(null,"faltan llenar datos");


    }//GEN-LAST:event_btnRegirtrarActionPerformed

    private void btnEliminacionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminacionActionPerformed

        String idCategoria = JOptionPane.showInputDialog("Id de la categoria: ");
        try {
            Categoria cat = CategoriaCN.getInstancia().BuscarCategoria(idCategoria);
            if (cat != null) {
                txtIdCategoria.setText(cat.getIdCategoria());
                txtDes.setText(cat.getDescripcion());
                txtNombre.setText(cat.getNombre());
                int respuesta = JOptionPane.showConfirmDialog(rootPane, "Deseas eliminar?", "Eliminar", JOptionPane.OK_CANCEL_OPTION);
                if (respuesta == JOptionPane.OK_OPTION) {
                    CategoriaCN.getInstancia().eliminarCategoria(cat);
                }
                limpiarTextos();
                buscar();
                textos(false);
            } else {
                JOptionPane.showMessageDialog(rootPane, "Categoria no existe.");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
    }//GEN-LAST:event_btnEliminacionActionPerformed

